let mensaje = "Pruebas de tipos de datos";
console.log(mensaje);

// String
let cadena = "Mi estudiantes son los mejores :)";
console.log("Tipo dato de la variable cadena: ", typeof cadena);

// Number
let valor = 25;
console.log("Tipo dato de la variable valor: ", typeof valor);

// Decimal
let dolar = 25.5;
console.log("Tipo dato de la variable dolar: ", typeof dolar);

// Booleano
let luz = false; //ecendida true, false apagada
console.log("Tipo dato de la variable luz: ", typeof luz);

// Indefinido (sino se asigna un valor)
let vacio;
console.log("Tipo dato de la variable vacio: ", typeof vacio);
